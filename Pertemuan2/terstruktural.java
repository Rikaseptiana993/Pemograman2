
package Pertemuan2;

public class terstruktural {
   public static void main(String[] args){
        
       //Variabel Luas Lingkaran
       double phi = 3.14;
       double jari2 = 10;
       
       //Variabel Luas Persegi Panjang
       double lebar = 10;
       double panjang = 20;
       
       //hitung luas Lingkaran
       double luasL = phi * (jari2 * jari2);
       
       //hitung luas Persegi panjang
       double luasP = lebar * panjang;
        
       System.out.println("luas lingkaran = " + luasL);
       System.out.println("luas lingkaran = " + luasP);
       
   }
    
}
